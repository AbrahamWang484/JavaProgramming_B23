package day06_UnaryOperators;

public class UnaryOperators {

    public static void main(String[] args) {
        int a = 100;
        ++a;

        System.out.println("a = " + a);


        int b = 100;
        --b;

        System.out.println("b = " + b);

        System.out.println("-----------------------------------");
        int x= 10;

        System.out.println( ++x); // 11 pre:



    }



}
